#!/usr/bin/env node
import fs from "fs"
import path from "path"
import { fileURLToPath } from "url"

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const blogsDirectory = path.join(__dirname, "../blog-posts")
const outputFile = path.join(__dirname, "../lib/blog-data.generated.ts")

function parseFrontmatter(content) {
  const frontmatterRegex = /^---\s*\n([\s\S]*?)\n---\s*\n([\s\S]*)$/
  const match = content.match(frontmatterRegex)

  if (!match) {
    return { frontmatter: {}, body: content }
  }

  const frontmatter = {}
  for (const line of match[1].split("\n")) {
    const colonIndex = line.indexOf(":")
    if (colonIndex > 0) {
      const key = line.substring(0, colonIndex).trim()
      let value = line.substring(colonIndex + 1).trim()
      if (
        (value.startsWith('"') && value.endsWith('"')) ||
        (value.startsWith("'") && value.endsWith("'"))
      ) {
        value = value.slice(1, -1)
      }
      frontmatter[key] = value
    }
  }

  return { frontmatter, body: match[2] }
}

function getSlugFromFilename(filename) {
  const match = filename.match(/^\d+-\d{4}-\d{2}-\d{2}-(.+)\.md$/)
  if (match) return match[1]
  return filename.replace(/\.md$/, "")
}

function buildPosts() {
  if (!fs.existsSync(blogsDirectory)) {
    return []
  }

  const posts = []

  for (const fileName of fs.readdirSync(blogsDirectory)) {
    if (!fileName.endsWith(".md") || fileName === "README.md") continue

    const fileContents = fs.readFileSync(
      path.join(blogsDirectory, fileName),
      "utf8"
    )
    const { frontmatter, body } = parseFrontmatter(fileContents)
    const slug = getSlugFromFilename(fileName)

    const excerptMatch = body.match(/^[^\n]+/)
    const excerpt = excerptMatch
      ? excerptMatch[0].substring(0, 200).replace(/^#+\s*/, "")
      : body.substring(0, 200)

    posts.push({
      slug,
      title: frontmatter.title || fileName.replace(/\.md$/, ""),
      author: frontmatter.author || "",
      date: frontmatter.date || "",
      source: frontmatter.source,
      content: body,
      excerpt: excerpt.length < body.length ? excerpt + "..." : excerpt,
    })
  }

  return posts.sort((a, b) => {
    return new Date(b.date).getTime() - new Date(a.date).getTime()
  })
}

const posts = buildPosts()
const fileContents = `// This file is auto-generated by scripts/generate-blog-data.mjs — do not edit.
import type { BlogPost } from "./blog-types"

export const ALL_BLOG_POSTS: BlogPost[] = ${JSON.stringify(posts, null, 2)}
`

fs.writeFileSync(outputFile, fileContents, "utf8")
console.log(`Generated ${posts.length} blog posts -> lib/blog-data.generated.ts`)
